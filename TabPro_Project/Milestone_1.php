<h3>Mission:</h3>
TabPro aims to allow guitar enthusiasts of all levels and ages to quickly create, share, and store custom guitar tabs on their profile. It is similar to SoundCloud, but for written guitar tabs.
<h3>Audience:</h3>
1) A guitarist who wants to write and store their own guitar music <br>
2) One who wants to explore/learn others’ original guitar tabs<br>
3) A learning musician who wants to tab out existing songs<br>
4) Someone who wants a place to store songs to play later<br>
5) Someone in a band that wants to easily collaborate, teach, or rehearse their songs<br>
<h3>Features:</h3>
Tab creation,  attach video/recording, comment, like, search your own database, search other people’s profiles, potentially a chord view and a note view

<h3>Page Breakdown:</h3>
    <div class="color">Create account– </div>
    &nbsp&nbsp create your profile, playlist section, and your own database for guitar tabs<br>
    <div class="color">Login– </div>
    &nbsp&nbsp if you have an account, the login page to enter your database<br>
    <div class="color">Tab creator– </div>
    &nbsp&nbsp (editing/creating page) write chords and notes using our simple interface<br>
    &nbsp&nbsp&nbsp&nbsp Recordings, videos, lyric options, comments, likes<br>
    <div class="color">Save Tab page (optional)–</div>
    &nbsp&nbsp when you want to save a tab (insert it into the database) the info you need to fill out to save it<br>
    <div class="color">Tab page– (not editing version, view page) </div>
    &nbsp&nbsp view of song tabs whether these are your own or someone else’s, you can comment or like<br>
    <div class="color">Personal Profiles– </div>
    &nbsp&nbsp sortable/editable database with playlists, individual songs with song info<br>
    <div class="color">Browse/Explore–  </div>
    &nbsp&nbsp search other people’s custom tabs and click on their profiles<br>
    <div class="color">Other’s Profiles– </div>
    &nbsp&nbsp search a profile or click on it through the browse page you will see a similar user interface as the Personal Profile with playlists, songs, and song info<br>
    <div class="color">Feed (optional)– </div>
    &nbsp&nbsp for users that you follow

<h3>Data Import:</h3>
Song title, artist, date created, genre, category, tab data, rating, capo, lyrics, comments, audio recording/video, username, password, first name, last name, email, photo (we won’t be inputting data - the user will input all their own data, we will create some examples though)

