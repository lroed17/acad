<div class="container1">
    <strong>The site's focus:</strong><br><br>

The site would be focusing on materials. We would be using what we learned in our materials class alongside additional research to create a database of the properties and technical specifications of different materials and their common uses. The user would be able to search what needs they want their material to meet (e.g. wants a strong, but bendy material) then we should show them their options. OR the user could search the material directly (e.g metal) and we would show them the different types of metal and their properties.
    <br><br>
    <strong>The audience:</strong><br><br>
    The audience for the project would be anyone who wants to build something and needs to choose the right material. We will give them the info they need to make this possible.
    <br><br>
    <strong>The data breakdown:</strong><br><br>
    The data would include: the material type, name, density, price, melting point, recycle potential, hardness, and a variety of other applicable features along with a brief description and possibly an image. We could also add a list of common use-cases for the product as well. This data would be flexible and may include other categories, but these would be the core topics for each search result.
<br><br>
    <a href="http://www.matweb.com"> Matweb </a>is a decent example of the concept I am going for, expect it is designed terribly and is hard to read unless you know how to navigate the page. It is also extremely details and complex - our site would be more for beginner builders with more basic properties and would be more legible and user friendly.
